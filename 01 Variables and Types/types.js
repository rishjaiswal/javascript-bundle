/*
JS is dynamic language,and therefore every variable is defined using the var keyword, 
and can contain all types of variables. */

var myNumber = 3;                   // a number
console.log(myNumber);
var myNumber = "Hello, World!"      // a string
console.log(myNumber);
var myNumber = true;                // a boolean
console.log(myNumber);


var mmyNumber = [];                    // an array
console.log(myNumber);
var myNumber = {};                  // an object
console.log(myNumber);


var mmyNumber = null                   // an null value
console.log(myNumber);

